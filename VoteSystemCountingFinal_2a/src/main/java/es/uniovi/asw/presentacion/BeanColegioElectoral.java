package es.uniovi.asw.presentacion;

public class BeanColegioElectoral {

	
	private String idColegio;
	private String numVotos;
	private String idOpcion;
	
	public String getIdOpcion() {
		return idOpcion;
	}

	public void setIdOpcion(String idOpcion) {
		this.idOpcion = idOpcion;
	}
	
	public String getIdColegio() {
		return idColegio;
	}

	public void setIdColegio(String idColegio) {
		this.idColegio = idColegio;
	}

	public String getNumVotos() {
		return numVotos;
	}

	public void setNumVotos(String numVotos) {
		this.numVotos = numVotos;
	}

	
	
	
}
